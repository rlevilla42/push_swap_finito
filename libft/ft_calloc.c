#include <stdio.h>
#include <stdlib.h>
#include "libft.h"

void	*ft_calloc(size_t n_elements, size_t size_datatype)
{
	void	*array;

	array = malloc(size_datatype * n_elements);
	if (!array)
	{
		return (NULL);
	}
	ft_bzero(array, n_elements * size_datatype);
	return (array);
}
